public class Employment {

    private String workName;
    private String positionName;
    private String durationEmployed;
    private String workDuties;

    public String getWorkName() {
        return workName;
    }

    public void setWorkName(String workName) {
        this.workName = workName;
    }

    public String getPositionName() {
        return positionName;
    }

    public void setPositionName(String positionName) {
        this.positionName = positionName;
    }

    public String getDurationEmployed() {
        return durationEmployed;
    }

    public void setDurationEmployed(String durationEmployed) {
        this.durationEmployed = durationEmployed;
    }

    public String getWorkDuties() {
        return workDuties;
    }

    public void setWorkDuties(String workDuties) {
        this.workDuties = workDuties;
    }

}
